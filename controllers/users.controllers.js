const User = require("../models/User.js");
const Access = require("../models/Access.js");
const crypto = require("crypto");
const { ObjectId } = require("mongoose").Types;
/**
 * Creates a new user, likely at sign in
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const createUser = async (req, res) => {
    const { firstName, lastName, email, password, accessCode } = req.body;

    try {
        let user = await User.findOne({ email: new RegExp(email, "i") });
        if (user) {
            return res.status(400).json({ message: "User already exists." });
        }

        let access = "novice";

        if (accessCode == process.env.ACCESS_CODE) {
            access = "admin";
        } else {
            const accessResult = await Access.findOne({
                accessCode: accessCode,
            });

            if (!accessResult) {
                return res
                    .status(400)
                    .send({ message: "Access code incorrect." });
            }

            access = accessResult.role;
        }
        user = new User({
            _id: new ObjectId(), // autogenerated from mongodb, not used directly
            uuid: crypto.randomUUID(),
            firstName,
            lastName,
            email: email.toLowerCase(),
            password,
            access,
        });

        await user.save();

        return res
            .status(200)
            .json({ message: "User registered successfully." });
    } catch (err) {
        console.error(err.message);
        return res.status(500).send({
            message: "Error with registering new user",
            error: err.message,
        });
    }
};

/**
 * Deletes a user from MongoDB.
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const deleteUserById = async (req, res) => {
    const uuid = req.params?.uuid;

    try {
        if (uuid) {
            const user = await User.findOneAndDelete({ uuid: uuid });
            if (!user) {
                return res.status(404).send({ message: "User not found." });
            }

            return res
                .status(200)
                .json({ message: "Successfully deleted user." });
        } else {
            return res.status(400).send({ message: "Missing User ID." });
        }
    } catch (err) {
        console.error(err.message);
        return res
            .status(500)
            .send({ message: "Error when deleting user.", error: err.message });
    }
};

/**
 * Updates a user in MongoDB.
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const updateUserById = async (req, res) => {
    const uuid = req.params?.uuid;
    try {
        if (uuid) {
            const user = await User.findOneAndUpdate({ uuid: uuid }, req.body);
            if (!user) {
                return res.status(404).send({ message: "User not found." });
            }

            const userObj = user.toObject();
            delete userObj._id;

            return res.status(200).json(userObj);
        } else {
            return res.status(400).send({ message: "Missing User ID." });
        }
    } catch (err) {
        console.error(err.message);
        return res
            .status(500)
            .send({ message: "Error when updating user.", error: err.message });
    }
};

/**
 * Retrieves a user from MongoDB based on id.
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const getUserById = async (req, res) => {
    const uuid = req.params?.uuid;

    try {
        if (uuid) {
            const user = await User.findOne(
                { uuid: uuid },
                { projection: { _id: 0, password: 0 } },
            );
            if (!user) {
                return res.status(404).send("User not found.");
            }

            return res.status(200).json({ user });
        } else {
            return res.status(400).send({ message: "Missing User ID." });
        }
    } catch (error) {
        console.error("Error fetching user:", error);
        return res.status(500).send({
            message: "Error when retrieving user.",
            error: err.message,
        });
    }
};

/**
 * Gets all issues from MongoDB based on filters.
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const getAllUsers = async (req, res) => {
    const { access } = req.query;
    try {
        let filter = {};
        if (access) {
            const accessArray = access.split(",").map((a) => a.trim());
            filter.access = { $in: accessArray };
        }
        const user = await User.find(filter, {
            projection: { _id: 0, password: 0 },
        }).sort({ firstName: 1 });
        return res.status(200).json(user);
    } catch (err) {
        return res.status(500).send({ message: err.message });
    }
};

/**
 * retrieves all user emails from MongoDB
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const getEmails = async (req, res) => {
    try {
        const users = await User.find(
            {},
            { projection: { _id: 0, password: 0 } },
        );

        const emails = users.map((user) => user.email).join(",");

        res.setHeader(
            "Content-Disposition",
            'attachment; filename="emails.txt"',
        );
        res.setHeader("Content-Type", "text/plain");
        return res.status(200).send(emails);
    } catch (err) {
        return res.status(500).send({ message: err.message });
    }
};

/**
 * registers additional user details during first-time setup
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const firstTimeSetup = async (req, res) => {
    try {
        const { uuid } = req.user; // `req.user` is set by Passport
        if (uuid) {
            const user = await User.findOne(
                { uuid: uuid },
                { projection: { _id: 0, password: 0 } },
            );
            if (!user) {
                return res.status(404).send({ message: "User not found." });
            }
            const { status, graduationYear, departments } = req.body;
            if (!status || !departments) {
                return res
                    .status(400)
                    .send({ message: "Missing required fields." });
            }

            user.status = status;
            if (graduationYear) {
                user.graduationDate = new Date(graduationYear, 5, 30); // June 30th of graduation year
            }
            user.departments = departments;
            await user.save();
            return res
                .status(200)
                .json({ message: "User updated successfully." });
        }
    } catch (err) {
        return res.status(500).send({ message: err.message });
    }
};

/**
 * allows a user to change their password by providing the current password and new password, with validation
 * @param {*} req - request details
 * @param {*} res - response details
 * @returns - response details (with status)
 */
const changePassword = async (req, res) => {
    const { currentPassword, newPassword } = req.body;
    if (!currentPassword || !newPassword) {
        return res
            .status(400)
            .json({ message: "Both current and new passwords are required." });
    }
    try {
        const user = await User.findOne({ uuid: req.user.uuid }).select(
            "+password",
        ); // `req.user` is set by Passport
        if (!user) {
            return res.status(404).json({ message: "User not found." });
        }

        const isMatch = await user.comparePassword(currentPassword);
        if (!isMatch) {
            return res
                .status(400)
                .json({ message: "Current password is incorrect." });
        }

        await user.setPassword(newPassword);
        await user.save();

        req.login(user, (err) => {
            if (err) {
                return res
                    .status(500)
                    .json({ message: "Session update failed." });
            }
            res.status(200).json({ message: "Password updated successfully." });
        });
    } catch (err) {
        console.error(err);
        res.status(500).json({ message: "Something went wrong." });
    }
};

module.exports = {
    createUser,
    deleteUserById,
    updateUserById,
    getUserById,
    getAllUsers,
    getEmails,
    firstTimeSetup,
    changePassword,
};
